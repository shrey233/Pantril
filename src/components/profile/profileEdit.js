import React from 'react';
import { Edit, TextInput, SimpleForm, required } from 'react-admin';
//
// Code referenced from Admin React Router djhi marmelab
//React Admin Advanced recipes
const ProfileEdit = ({ staticContext, ...props }) => {
    return (
        <Edit
            /*
                As we are not coming for a route generated by a Resource component,
                we have to provide the id ourselves.
                As there is only one config for the current user, we decided to
                hardcode it here
            */
            id="my-profile"
            /*
                For the same reason, we need to provide the resource and basePath props
                which are required by the Edit component
            */
            resource="profile"
            basePath="/my-profile"
            redirect={false}
            title="My profile"
            {...props}
        >
            <SimpleForm>
                <TextInput source="firstname" validate={required()} />
                <TextInput source="surname" validate={required()} />
                <TextInput source="Email" validate={required()} />
                <TextInput source="Allergies" validate={required()} />
                <TextInput source="Recipes" validate={required()} />
                <TextInput source="ingredients" validate={required()} />
                <TextInput source="dietry needs" validate={required()} />
            </SimpleForm>
        </Edit>
    );
};

